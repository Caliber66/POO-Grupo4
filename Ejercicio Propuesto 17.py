# 17. Dado el radio de un círculo. Haga un algoritmo que obtenga el área del círculo y la longitud de la circunferencia.

Radio = int(input('Radio de el circulo: '))
Area = (3.141592*(Radio**2))
Circunferencia = ((2*3.141592)*Radio)
print('El area es:', str(Area), 'La circunferencia es:', str(Circunferencia))